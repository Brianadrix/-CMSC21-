#include <stdio.h>
#include <stdlib.h>

int main()
{
    int x, y, sub_result; // declares a variable //

    // gives value to the variables //
    x = 15;
    y = 87;

    // formula for subtraction //
    sub_result = x-y;

    // displays the result in the terminal //
    printf("The subtraction of %d and %d is %d", x, y, sub_result);

    return 0;
}
